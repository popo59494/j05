int	ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	while (str[i] > 64 && str[i] < 91)
	{
		i = i + 1;
		if (str[i] == '\0')
			return (1);
	}
	if (str[0] == '\0')
	   return (1);
	return (0);	
}
