int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i] !='\0')
		i = i + 1;
	while (s2[j] != '\0')
		j = j + 1;
	if (i > j)
		return (1);
	if (i < j)
		return (-1);
	if (i == j)
		return (0);
}
