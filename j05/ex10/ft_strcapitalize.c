char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ' ')
			if (str[i+1] > 96 && str[i+1] < 123)
				str[i+1] = (str[i+1] - 32);
		i = i + 1;
	}
	return (str);
}
