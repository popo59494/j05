char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i < n)
	{
		if (src[i] == '\0')
			i = i + 1;
		dest[i] = src[i];
		i = i + 1;
	}
	return (dest);
}
