int	ft_atoi(char *str)
{
	int i;
	int res;
	int neg;

	i = 0;
	res = 0;
	neg = 1;
	while (str[i] < 33 || str[i] == 127 || str[i] == '+')
		i = i + 1;
	if (str[i] == 45)
	{
		neg = (-1);
		i = i + 1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i = i + 1;
	}
	return (res * neg);
}
